    public void preencherCampos(Pessoas pessoa) {
        txtCodigo.setText(String.valueOf(pessoa.getIdpessoa()));
        txtNome.setText(pessoa.getNome());
        txtEmail.setText(pessoa.getEmail());
        btnSalvar.setEnabled(false);
    }
    
    private void preencherCombo(int idpessoa) {
        Pessoas p = pBO.getPessoas(idpessoa);
        cmbPessoas.addItem(p.getNome() + " | " + p.getIdpessoa());
        preencherCampos(p);
    }  
    
